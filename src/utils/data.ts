export const camelizeKeys = <T>(obj: T): T => {
  if (Array.isArray(obj)) {
    // @ts-expect-error
    return obj.map((v) => camelizeKeys(v));
  } else if (obj != null && obj.constructor === Object) {
    // @ts-expect-error
    return Object.keys(obj).reduce(
      (result, key) => ({
        ...result,
        // @ts-expect-error
        [camelCase(key)]: camelizeKeys(obj[key]),
      }),
      {},
    );
  }
  return obj;
};

